export const updateSubjectName = (subjectName) => {
  if (subjectName === "Programowanie zaawansowane 2") {
    return "PZ2";
  }

  if (subjectName === "Uczenie maszynowe") {
    return "ML";
  }

  if (subjectName === "Sieci komputerowe") {
    return "Sieci";
  }

  if (subjectName === "Modele wytwarzania oprogramowania") {
    return "Modele";
  }

  if (subjectName === "Data Engineering") {
    return "Data";
  }

  if (subjectName === "Teoria kompilacji i kompilatory") {
    return "TKiK";
  }

  if (subjectName === "Rachunek prawdopodobieństwa i statystyka") {
    return "Rachunek";
  }

  if (subjectName === "Matematyka dyskretna") {
    return "Dyskretna";
  }

  if (subjectName === "Teoria grafów") {
    return "Grafy";
  }

  if (subjectName === "Podstawy programowania 2") {
    return "C++";
  }

  if (subjectName === "Algorytmy i struktury danych") {
    return "Algorytmy";
  }

  if (subjectName === "Podstawy grafiki komputerowej") {
    return "Grafika";
  }

  if (subjectName === "Wprowadzenie do systemów inteligentnych") {
    return "WDSI";
  }

  if (subjectName === "Badania operacyjne") {
    return "BO";
  }
  return subjectName;
};
